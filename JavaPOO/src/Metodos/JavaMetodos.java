package Metodos;

public class JavaMetodos {
	public int idade(int ano) { 
		return 2024-ano;
	}
	public double valor(double valorHora, int horas) {
		return valorHora * horas;
	}
	public String nome(String nomeUsuario) {
		return nomeUsuario;
	}
	public boolean estado(int anos) {
		if (anos >= 3) {
			return true;
		}
		else {
			return false;
		}
	}
}